MILLION = 1_000_000
BINARY_NUMBER = 0b1010_0011
OCTAL_NUMBER = 0o755_644
HEX_NUMBER = 0xFF_FF_FF

print(f"million: {MILLION:,} binary: {bin(BINARY_NUMBER)} octal: {oct(OCTAL_NUMBER)} hex: {hex(HEX_NUMBER)}")


# You can also use the remove() method to remove an element from the array.

# Example
# Delete the element that has the value "Volvo":

cars = ["Ford", "Volvo", "BMW"]
print("before remove", cars)
cars.remove("Volvo")
print("after remove", cars)

# $ python3 remove-element-2.py 
# before remove ['Ford', 'Volvo', 'BMW']
# after remove ['Ford', 'BMW']
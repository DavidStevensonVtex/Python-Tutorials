# type()
# From Python's perspective, dictionaries are defined as objects with the data type 'dict':

# <class 'dict'>
# Example
# Print the data type of a dictionary:

thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
print(type(thisdict))

# $ python3 dictionary-type.py
# <class 'dict'>
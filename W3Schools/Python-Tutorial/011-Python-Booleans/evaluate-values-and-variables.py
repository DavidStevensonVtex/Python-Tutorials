# Evaluate Values and Variables

# The bool() function allows you to evaluate any value, and give you True or False in return,

# Example
# Evaluate a string and a number:

print(bool("Hello"))
print(bool(15))

# Example

# Evaluate two variables:

x = "Hello"
y = 15

print(bool(x))
print(bool(y))


# Access the Elements of an Array
# You refer to an array element by referring to the index number.

# Example
# Get the value of the first array item:

cars = ["Ford", "Volvo", "BMW"]
print(cars)

x = cars[0]
print(x)

# Example
# Modify the value of the first array item:

cars[0] = "Toyota"
print(cars)

# ['Ford', 'Volvo', 'BMW']
# Ford
# ['Toyota', 'Volvo', 'BMW']
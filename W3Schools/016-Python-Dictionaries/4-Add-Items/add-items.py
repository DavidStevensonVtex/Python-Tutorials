# Adding Items
# Adding an item to the dictionary is done by using a new index key and assigning a value to it:

thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
thisdict["color"] = "red"
print(thisdict)

# $ python3 add-items.py 
# {'brand': 'Ford', 'model': 'Mustang', 'year': 1964, 'color': 'red'}
# Change Values
# You can change the value of a specific item by referring to its key name:

# Change the "year" to 2018:

thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
thisdict["year"] = 2018
print(thisdict)

# $ python3 change-values.py 
# {'brand': 'Ford', 'model': 'Mustang', 'year': 2018}